int	ft_str_is_alpha(char *str)
{
	int		x;

	x = 0;
	while (str[x] != '\0')
	{
		if (str[x] == 0)
			return (1);
		if ((str[x] < 65) || (str[x] > 122) || ((str[x] > 90) && (str[x] < 97)))
			return (0);
		x++;
	}
	return (1);
}
